    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        // TODO add your handling code here:

        NhaBLL nhaBLL = new NhaBLL();
        QuyDinhThoiGianBLL quyDinhThoiGianBLL = new QuyDinhThoiGianBLL();
        QuyDinhThoiGianEntity quyDinhThoiGianEntity = quyDinhThoiGianBLL.LayThoiGian();
        XepPhongBLL xepPhongBLL = new XepPhongBLL();
        LoaiNhaBLL loaiNhaBLL = new LoaiNhaBLL();

        String tenNha = tf_Nha.getText().trim();
        String gioiTinh = cb_GioiTinh.getSelectedItem().toString().trim();
        int soNguoi = Integer.parseInt(cb_SoNguoi.getSelectedItem().toString().trim());
        int maLoaiNha = loaiNhaBLL.LayMaLoaiNha(soNguoi, gioiTinh);

        if (!tb_Nha.isColumnSelected(1) && !tb_Nha.isColumnSelected(2) && !tb_Nha.isColumnSelected(0)) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhà ở bảng phía dưới sau đó cập nhật lại thông tin ở trên.");
            return;
        }

        if (tf_Nha.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà.");
            return;
        }

        if (cb_GioiTinh.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giới tính.");
            return;
        }

        if (cb_SoNguoi.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn số người.");
            return;
        }

        int index = tb_Nha.getSelectedRow();
        int maNha = (int) tb_Nha.getValueAt(index, 0);

        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn thay đổi thông tin nhà được chọn?");
        if (choice == JOptionPane.YES_OPTION) {

            if (xepPhongBLL.KiemTraXepPhong(maNha, quyDinhThoiGianEntity.getNamHoc())) {
                JOptionPane.showMessageDialog(this, "Không thể sửa .\n Nhà này đang được sử dụng trong năm học hiện tại.");
                return;
            }
            nhaBLL.Update(tenNha, maNha, maLoaiNha);
            ShowTable();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công.");
        }


    }//GEN-LAST:event_btn_SuaActionPerformed

