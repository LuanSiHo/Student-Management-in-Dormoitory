    private void ShowTable() {
        Vector cols = new Vector();
        Vector data = new Vector();
        NhaBLL nhaBLL = new NhaBLL();

        nhaBLL.ShowTable(cols, data);
        tb_Nha.setModel(new AbstractTableModel() {
            @Override
            public int getRowCount() {
                return data.size();
            }

            @Override
            public int getColumnCount() {
                return cols.size();
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                Vector rowData = (Vector) (data.elementAt(rowIndex));
                return rowData.elementAt(columnIndex);
            }

            @Override
            public String getColumnName(int column) {
                return (String) cols.elementAt(column);
            }
        });
    }

    private void showComboBoxGioiTinh() {
        LoaiNhaBLL loaiNhaBLL = new LoaiNhaBLL();
        ArrayList<String> danhsachGioiTinh = loaiNhaBLL.LayGioiTinh();
        for (int i = 0; i < danhsachGioiTinh.size(); i++) {
            cb_GioiTinh.addItem(danhsachGioiTinh.get(i));
        }
    }

    private void showComboBoxSoNguoi() {
        LoaiNhaBLL loaiNhaBLL = new LoaiNhaBLL();
        ArrayList<Integer> danhSachSoNguoi = loaiNhaBLL.LaySoNguoi();
        for (int i = 0; i < danhSachSoNguoi.size(); i++) {
            cb_SoNguoi.addItem(String.valueOf(danhSachSoNguoi.get(i)));
        }
    }

