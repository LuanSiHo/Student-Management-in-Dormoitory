    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        NhaBLL nhaBLL = new NhaBLL();
        LoaiNhaBLL loaiNhaBLL = new LoaiNhaBLL();

        if (tf_Nha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà.");
            return;
        }

        if (cb_GioiTinh.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giới tính.");
            return;
        }

        if (cb_SoNguoi.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn số người.");
            return;
        }

        int maLoaiNha = loaiNhaBLL.LayMaLoaiNha(Integer.parseInt(cb_SoNguoi.getSelectedItem().toString().trim()), cb_GioiTinh.getSelectedItem().toString().trim());
        String tenNha = tf_Nha.getText().trim();

        if (nhaBLL.TimNha(tenNha)) {
            JOptionPane.showMessageDialog(this, "Không thể thêm. Tên nhà đã có trong danh sách.");
            return;
        }
        nhaBLL.Insert(tenNha, maLoaiNha);
        JOptionPane.showMessageDialog(this, "Thêm Nhà thành công.");
        ShowTable();

    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:

        NhaBLL nhaBLL = new NhaBLL();
        PhongBLL phongBLL = new PhongBLL();
        XepPhongBLL xepPhongBLL = new XepPhongBLL();
        ThuPhiNoiTruBLL thuPhiNoiTruBLL = new ThuPhiNoiTruBLL();
        GhiNhanDienNuocBLL ghiNhanDienNuocBLL = new GhiNhanDienNuocBLL();
        ThuPhiDienNuocBLL thuPhiDienNuocBLL = new ThuPhiDienNuocBLL();
        QuyDinhThoiGianBLL quyDinhThoiGianBLL = new QuyDinhThoiGianBLL();
        QuyDinhThoiGianEntity quyDinhThoiGianEntity = quyDinhThoiGianBLL.LayThoiGian();

        String tenNha = tf_Nha.getText().trim();
        int maNha = nhaBLL.LayMaNha(tenNha);

        if (tf_Nha.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhà hoặc chọn nhà ở bảng phía dưới.");
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa nhà " + tenNha + " khỏi danh sách?");
        if (choice == JOptionPane.YES_OPTION) {
            if (nhaBLL.TimNha(tenNha) == false) {
                JOptionPane.showMessageDialog(this, "Không thế xóa.\nNhà này không có trong danh sách.");
                return;
            }

            if (xepPhongBLL.KiemTraXepPhong(maNha, quyDinhThoiGianEntity.getNamHoc())) {
                JOptionPane.showMessageDialog(this, "Không thể xóa.\n Nhà này đang được sử dụng trong năm học hiện tại.");
                return;
            }
            thuPhiNoiTruBLL.delete(quyDinhThoiGianEntity.getNamHoc());
            xepPhongBLL.Delete(maNha);
            ghiNhanDienNuocBLL.Delete(maNha);
            thuPhiDienNuocBLL.Delete(maNha);
            phongBLL.Delete(maNha);
            nhaBLL.Delete(tenNha);
            ShowTable();
            JOptionPane.showMessageDialog(this, "Xóa thành công " + tenNha + " khỏi danh sách");
        }

    }//GEN-LAST:event_btn_XoaActionPerformed

